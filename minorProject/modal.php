        <h2 class="lead" style=" text-indent: 50px;"><strong> Indira Gandhi Delhi Technical University for Women    </strong>
</h2>
 <p class="lead" style=" text-indent: 50px;">
Indira Gandhi Delhi Technical University for Women (IGDTUW) has been upgraded from Indira Gandhi Institute of Technology in May 2013 vide Delhi State Legislature Act 9, 2012, as a non-affiliating teaching and research University at Delhi to facilitate and promote studies, research, technology, innovation, incubation and extension work in emerging areas of professional education among women, with focus on engineering, technology, applied sciences, management and its allied areas with the objective to achieve excellence in these and related fields.
</p>
 <p class="lead" style=" text-indent: 50px;">
Erstwhile Indira Gandhi Institute of Technology (IGIT) was established in 1998 by Directorate of Training and Technical Education, Govt of N.C.T of Delhi as the first engineering college for women only. From 2002- 2013, the college remained the first and only constituent college of Guru Gobind Singh Indraprastha University.
</p>
 <p class="lead" style=" text-indent: 50px;">
The upgradation of IGIT, already a prestigious college of north India, to University has enabled it to function even more efficiently as a teaching and research centre in various branches of engineering, promoting advancement and dissemination of knowledge and learning among women of the country and to meet the requirement of technically skilled women workforce. The objective of the University is to foster industry relevant research and innovations  and empower the women of our country through value based higher education making them employable, self reliant, responsible citizen of the country with concern for environment and society.
</p>
